// Preprocessor directive
//stdio - standard input output
#include <stdio.h>

//Create a main function

int main(){
    //Create a variable
    int x = 10;
    int y = 69;
    printf("x time %d\n", x);
    printf("y time %d\n", y);
    //converse specifier %d, puts the x where the %d is

    //sum time
    int sum = x + y;
    printf("%d+%d=%d\n", x, y, sum);
   
   
    return 0;

    
}